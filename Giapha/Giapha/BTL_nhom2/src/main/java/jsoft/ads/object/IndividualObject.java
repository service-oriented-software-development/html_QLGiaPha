package jsoft.ads.object;

public class IndividualObject {
	private int individual_id ;
	private String fullname ;
	private int gender ;
	private String date_of_birth ;
	private String date_of_death ;
	private int father ;
	private String branch ;
	private int parentage_id;
	
	public int getIndividual_id() {
		return individual_id;
	}
	public void setIndividual_id(int individual_id) {
		this.individual_id = individual_id;
	}
	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getDate_of_birth() {
		return date_of_birth;
	}
	public void setDate_of_birth(String date_of_birth) {
		this.date_of_birth = date_of_birth;
	}
	public String getDate_of_death() {
		return date_of_death;
	}
	public void setDate_of_death(String date_of_death) {
		this.date_of_death = date_of_death;
	}
	public int getFather() {
		return father;
	}
	public void setFather(int father) {
		this.father = father;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public int getParentage_id() {
		return parentage_id;
	}
	public void setParentage_id(int parentage_id) {
		this.parentage_id = parentage_id;
	}
	
	
}
